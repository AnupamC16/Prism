func TestGetHLS_DifferentFiltersDifferentCacheKeys(t *testing.T) {
	svc, _, hlsGen, _ := setupManifestService(t)
	reqA := &model.ManifestRequest{AssetID: "asset-1", Codec: "avc1"}
	reqB := &model.ManifestRequest{AssetID: "asset-1", Codec: "hvc1"}

	if _, err := svc.GetHLS(context.Background(), reqA); err != nil {
		t.Fatalf("GetHLS reqA returned error: %v", err)
	}
	if _, err := svc.GetHLS(context.Background(), reqB); err != nil {
		t.Fatalf("GetHLS reqB returned error: %v", err)
	}
	if hlsGen.calls != 2 {
		t.Fatalf("expected two generator calls for different filters, got %d", hlsGen.calls)
	}
}
