type mockGenerator struct {
	output []byte
	err    error
	calls  int
}

func (g *mockGenerator) Generate(_ context.Context, _ *model.ManifestRequest) ([]byte, error) {
	g.calls++
	return g.output, g.err
}

func setupManifestService(t *testing.T) (*service.ManifestService, *mock.MockCache, *mockGenerator, *mockGenerator) {
	t.Helper()

	logger := slog.New(slog.NewTextHandler(io.Discard, nil))
	cfg := &config.Config{
		ManifestCacheTTL:     30,
		CloudFrontDomain:     "cdn.example.com",
		CloudFrontKeyPairID:  "KTEST123",
		CloudFrontPrivateKey: testCloudFrontPrivateKey,
	}
	cloudFront, err := cdn.NewCloudFront(cfg, logger)
	if err != nil {
		t.Fatalf("create CloudFront test client: %v", err)
	}

	cacheMock := mock.NewMockCache()
	hlsGen := &mockGenerator{output: []byte("#EXTM3U\nmanifest body")}
	dashGen := &mockGenerator{output: []byte(`<?xml version="1.0"?><MPD></MPD>`)}
	svc := service.NewManifestService(hlsGen, dashGen, cacheMock, cloudFront, cfg, logger)
	return svc, cacheMock, hlsGen, dashGen
}

func TestGetHLS_CacheHit(t *testing.T) {
	svc, cacheMock, hlsGen, _ := setupManifestService(t)
	req := &model.ManifestRequest{AssetID: "asset-1"}
	key := cache.ManifestKey("hls", req.AssetID, req.FilterHash())
	want := []byte("cached-hls-manifest")
	if err := cacheMock.Set(context.Background(), key, want, time.Minute); err != nil {
		t.Fatalf("seed cache: %v", err)
	}

	got, err := svc.GetHLS(context.Background(), req)
	if err != nil {
		t.Fatalf("GetHLS returned error: %v", err)
	}
	if !bytes.Equal(got, want) {
		t.Fatalf("expected cached bytes %q, got %q", want, got)
	}
	if hlsGen.calls != 0 {
		t.Fatalf("expected generator not called, got %d calls", hlsGen.calls)
	}
}

